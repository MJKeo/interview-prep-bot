/**
 * Button variant types for styling.
 * Determines the visual appearance of the button.
 */
export enum ButtonType {
  /**
   * Primary button style - filled background with vintage-grape color and white text.
   */
  PRIMARY = "primary",
  /**
   * Secondary button style - vintage-grape border with clear background and vintage-grape text.
   */
  SECONDARY = "secondary",
}

